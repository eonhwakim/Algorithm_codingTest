/**
 * 문제 설명
n개의 음이 아닌 정수들이 있습니다. 이 정수들을 순서를 바꾸지 않고 적절히 더하거나 빼서 타겟 넘버를 만들려고 합니다. 예를 들어 [1, 1, 1, 1, 1]로 숫자 3을 만들려면 다음 다섯 방법을 쓸 수 있습니다.

-1+1+1+1+1 = 3
+1-1+1+1+1 = 3
+1+1-1+1+1 = 3
+1+1+1-1+1 = 3
+1+1+1+1-1 = 3
사용할 수 있는 숫자가 담긴 배열 numbers, 타겟 넘버 target이 매개변수로 주어질 때 숫자를 적절히 더하고 빼서 타겟 넘버를 만드는 방법의 수를 return 하도록 solution 함수를 작성해주세요.

제한사항
- 주어지는 숫자의 개수는 2개 이상 20개 이하입니다.
- 각 숫자는 1 이상 50 이하인 자연수입니다.
- 타겟 넘버는 1 이상 1000 이하인 자연수입니다.

입출력 예
numbers	target	return
[1, 1, 1, 1, 1]	3	5
[4, 1, 2, 1]	4	2

입출력 예 설명
입출력 예 #1
- 문제 예시와 같습니다.

입출력 예 #2
+4+1-2+1 = 4
+4-1+2-1 = 4
총 2가지 방법이 있으므로, 2를 return 합니다.
 */



function solution(numbers, target) {
  let answer = 0;

  // DFS (Depth-First Search, 깊이 우선 탐색) 함수 정의
  // index: 현재 처리 중인 숫자의 인덱스 (몇 번째 숫자인지)
  // sum: 현재까지 누적된 합계
  function dfs(index, sum) {
      // 1. 탈출 조건 (Base Case): 모든 숫자를 다 사용했을 때
      if (index === numbers.length) {
          // 현재까지의 합계가 타겟 넘버와 같다면 정답 카운트 증가
          if (sum === target) {
              answer++;
          }
          return;
      }

      // 2. 수행 동작 (Recursive Step):
      // 현재 숫자를 '더하는 경우'와 '빼는 경우' 두 갈래로 뻗어나갑니다.
      
      // 경우 1: 현재 숫자를 더하고 다음 숫자로 넘어감
      dfs(index + 1, sum + numbers[index]);
      
      // 경우 2: 현재 숫자를 빼고 다음 숫자로 넘어감
      dfs(index + 1, sum - numbers[index]);
  }

  // 탐색 시작: 0번째 인덱스부터, 합계 0으로 시작
  dfs(0, 0);

  return answer;
}

console.log(solution([1, 1, 1, 1, 1], 3)); // 5
console.log(solution([4, 1, 2, 1], 4)); // 2

/**
 * 문제 설명
초 단위로 기록된 주식가격이 담긴 배열 prices가 매개변수로 주어질 때, 
가격이 떨어지지 않은 기간은 몇 초인지를 return 하도록 solution 함수를 완성하세요.

제한사항
prices의 각 가격은 1 이상 10,000 이하인 자연수입니다.
prices의 길이는 2 이상 100,000 이하입니다.

입출력 예
prices	return
[1, 2, 3, 2, 3]	[4, 3, 1, 1, 0]

입출력 예 설명
1초 시점의 ₩1은 끝까지 가격이 떨어지지 않았습니다.
2초 시점의 ₩2은 끝까지 가격이 떨어지지 않았습니다.
3초 시점의 ₩3은 1초뒤에 가격이 떨어집니다. 따라서 1초간 가격이 떨어지지 않은 것으로 봅니다.
4초 시점의 ₩2은 1초간 가격이 떨어지지 않았습니다.
5초 시점의 ₩3은 0초간 가격이 떨어지지 않았습니다.
 */

function solution(prices) {
    const n = prices.length;
    const answer = new Array(n).fill(0); // 정답을 담을 배열 (0으로 초기화)
    const stack = []; // "아직 가격이 떨어지지 않은 시점(index)"들을 저장하는 곳

    for (let i = 0; i < n; i++) {
        // 1. 가격 하락 감지:
        // 스택에 과거 시점들이 있고, '현재 가격(prices[i])'이 '과거 가격(prices[top])'보다 낮으면?
        // => 가격이 떨어진 것!
        while (stack.length > 0 && prices[i] < prices[stack[stack.length - 1]]) {
            const j = stack.pop(); // 가격이 떨어진 과거 시점(j)을 꺼냄
            
            // 2. 기간 계산:
            // 떨어진 시점(i) - 과거 시점(j) = 가격이 유지된 기간
            answer[j] = i - j; 
        }

        // 3. 현재 시점 저장:
        // 아직 가격이 떨어지지 않았으므로 스택에 넣고 다음으로 넘어감
        stack.push(i);
    }

    // 4. 끝까지 가격이 안 떨어진 시점들 처리:
    // 반복문이 다 끝났는데도 스택에 남아있다면, 끝까지 가격이 안 떨어진 것임
    while (stack.length > 0) {
        const j = stack.pop();
        // 전체 기간(n-1)에서 해당 시점(j)을 뺌
        answer[j] = (n - 1) - j;
    }

    return answer;
}

console.log(solution([1, 2, 3, 2, 3])); // [4, 3, 1, 1, 0]
//이 코드는 배열을 한 번만 훑으면서(O(N)) 정답을 구해냅니다.

/**
 * '주식가격' 문제는 "내가 산 주식이 가격이 떨어지기 전까지 몇 초를 버텼는가?"를 구하는 문제입니다.
 * 
 * ### 문제 이해하기 (쉬운 설명)
예시: [1, 2, 3, 2, 3]

- 1초(1원) : 끝까지 1원 밑으로 안 떨어짐 → 4초간 유지
- 2초(2원) : 끝까지 2원 밑으로 안 떨어짐 → 3초간 유지
- 3초(3원) : 바로 다음 4초에 2원으로 떨어짐! → 1초간 유지 (3초에서 4초로 넘어가는 그 1초 동안은 3원이었음)
- 4초(2원) : 다음 5초에 3원(오름) → 1초간 유지
- 5초(3원) : 끝남 → 0초

### 효율적인 풀이 (스택 활용)
이 문제는 이중 반복문(O(N²))으로도 풀리지만, 
주식 가격 데이터가 많을 경우(최대 10만 개) 시간 초과가 날 수 있습니다. 
스택(Stack)을 사용하면 O(N)으로 훨씬 빠르게 해결할 수 있습니다.

핵심 아이디어 :
1. 스택에는 "가격이 아직 떨어지지 않은 시점(인덱스)"을 저장 합니다. 왜냐하면 "몇 초간 유지했는지"를 계산해야 하기 때문
2. 현재 가격이 스택의 맨 위(가장 최근 시점) 가격보다 떨어지면 , 그때 비로소 "아, 이 시점의 가격은 여기까지였구나" 하고 
기간을 계산 ( 현재시간 - 과거시간 )하여 꺼냅니다(pop).
3. 끝까지 스택에 남아있는 시점들은 "끝까지 가격이 안 떨어진 경우"이므로, 전체 길이에서 해당 시점을 빼서 계산합니다.
 
코드 설명: [1, 2, 3, 2, 3]
1. 반복문 시작(for문)
시점(i) | 가격 | 스택 상태 | 동작 설명
0초      1원     [0]     스택이 비었으므로 그냥 넣음(0초 시점 저장)
1초      2원     [0, 1]  2원은 1원(0초)보다 커서, 가격이 안떨어졌으니 넣음
2초      3원     [0, 1, 2] 3원은 2원(1초)보다 커서, 가격이 안떨어졌으니 넣음

2. 가격 하락 발생!(while문)
시점(i) | 가격 | 스택 상태 | 동작 설명
3초      2원    [0, 1, 2] 현재 2원은 스택 맨위(2초 시점의 3원)보다 싸다
                         -> 2초 시점은 3초에 가격이 떨어졌으므로 끝
                         -> answer[2] = 3-2 = 1초 (저장)
                         -> 스택에서 2 제거. 남은 스택: [0,1]
(계속)    2원    [0,1]    현재 2원은 스택 맨위(1초 시점의 2원)과 같음
                         -> 떨어진게 아니므로 통과
                         -> 3초 시점도 스택에 넣음 [0, 1, 3]   
                         
3. 나머지 진행
시점(i) | 가격 | 스택 상태 | 동작 설명
4초      3원    [0, 1, 3, 4] 3원은 2원(3초)보다 큽니다. 안 떨어졌으니 넣습니다.
                         
4. 끝까지 안 떨어진 애들 처리 ( while 문)
반복문이 끝났는데 스택에 [0, 1, 3, 4] 가 남아있습니다. 얘네는 끝까지 가격이 안 떨어진 대단한 녀석들입니다.

- 4초 시점 : answer[4] = 4(끝) - 4 = 0초
- 3초 시점 : answer[3] = 4(끝) - 3 = 1초
- 1초 시점 : answer[1] = 4(끝) - 1 = 3초
- 0초 시점 : answer[0] = 4(끝) - 0 = 4초
최종 결과 : [4, 3, 1, 1, 0]

### 핵심 요약
- 스택 은 "아직 가격이 떨어지지 않은 시점들"을 보관하는 창고입니다.
- 새로운 가격이 들어올 때마다 , 창고(스택)에 있는 과거 가격들과 비교합니다.
- 가격이 떨어졌다면? → "너는 여기까지다!" 하고 꺼내서(pop) 기간을 계산합니다.
- 끝까지 남았다면? → 전체 기간에서 내 시점을 빼서 계산합니다.
*/

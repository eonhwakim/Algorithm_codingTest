/** Lv1, 스택/큐
 * 문제 설명
배열 arr가 주어집니다. 배열 arr의 각 원소는 숫자 0부터 9까지로 이루어져 있습니다. 
이때, 배열 arr에서 연속적으로 나타나는 숫자는 하나만 남기고 전부 제거하려고 합니다. 
단, 제거된 후 남은 수들을 반환할 때는 배열 arr의 원소들의 순서를 유지해야 합니다. 예를 들면,

arr = [1, 1, 3, 3, 0, 1, 1] 이면 [1, 3, 0, 1] 을 return 합니다.
arr = [4, 4, 4, 3, 3] 이면 [4, 3] 을 return 합니다.
배열 arr에서 연속적으로 나타나는 숫자는 제거하고 남은 수들을 return 하는 solution 함수를 완성해 주세요.

제한사항
배열 arr의 크기 : 1,000,000 이하의 자연수
배열 arr의 원소의 크기 : 0보다 크거나 같고 9보다 작거나 같은 정수

입출력 예
arr |	answer
[1,1,3,3,0,1,1]	| [1,3,0,1]
[4,4,4,3,3]	| [4,3]

 * 
 */

function solution(arr) {
    let answer = [];
    for (const num of arr) {
        // answer 배열의 "마지막 원소"와 "현재 숫자(num)"가 다르면 추가
        // 즉, 방금 넣은 숫자와 똑같으면(중복이면) 넣지 않고 넘어감
        if (answer[answer.length - 1] !== num) {
            answer.push(num);
        }
    }
    return answer;
}

console.log(solution([1,1,3,3,0,1,1])); // [1, 3, 0, 1]


/**
 * 최적의 풀이 (filter 활용)
 * 이 문제는 배열의 순서를 유지하면서 연속적으로 나타나는 중복 숫자만 제거 하는 것입니다.
가장 직관적이고 효율적인 방법은 현재 숫자와 바로 다음 숫자 를 비교하는 것입니다.

1. 배열 순회 : 배열의 처음부터 끝까지 순회합니다.
2. 비교 조건 : 현재 원소( val )와 바로 다음 원소( arr[index + 1] )가 다를 때만 남깁니다.
3. 마지막 원소 처리 : 배열의 마지막 원소는 다음 원소가 undefined 이므로, 항상 다르다고 판단되어 결과에 포함됩니다.

###  시간 복잡도 및 효율성
- 시간 복잡도: O(N)
  - filter 함수는 배열을 한 번만 순회하므로, 입력 배열의 크기(N)에 비례하는 시간이 소요됩니다. (N 최대 1,000,000)
- 공간 복잡도: O(N)
  - 최악의 경우(중복이 없는 경우) 입력 배열과 동일한 크기의 새로운 배열을 반환합니다.
이 방식은 코드가 간결하며, 자바스크립트 엔진 내부적으로 최적화된 filter 메서드를 사용하므로 성능면에서도 우수합니다.

* filter 함수는 조건이 true 인 요소만 남겨서 새로운 배열을 만듭니다.
 */

function solution2(arr) {
  // 현재 원소와 바로 다음 원소가 다를 때만 남김
  // 마지막 원소는 다음 원소가 undefined 이므로 항상 포함
  return arr.filter((val, index) => val !== arr[index+1]); // 현재값과 다음값이 다르면 남긴다
}